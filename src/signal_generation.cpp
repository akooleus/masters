// ═══════════════════════════════════════════════════════════════
//  signal_generation.cpp
//  Генерация сигналов для верификации фильтров и бенчмарков.
//
//  Три вида сигналов:
//
//    1. Белый шум — равномерное распределение на [−1, +1].
//       Используется для оценки ОСШ и замеров быстродействия.
//       Спектр плоский, поэтому все частотные компоненты
//       представлены примерно одинаково.
//
//    2. Единичный импульс — δ[n]: единица в нулевом отсчёте,
//       нули в остальных.  Выход фильтра на импульс — его
//       импульсная характеристика h[n].  Используется для
//       прямого сравнения коэффициентов: если фильтрация
//       корректна, выход должен совпадать с h[n].
//
//    3. Мультитон — сумма синусоид заданных частот.
//       Используется для визуальной проверки АЧХ:
//       компоненты в полосе пропускания должны пройти,
//       компоненты в полосе заграждения — подавиться.
//
//  Генератор псевдослучайных чисел — xorshift32 (быстрый,
//  воспроизводимый, не зависит от реализации std::mt19937).
// ═══════════════════════════════════════════════════════════════

#include "cascade_fir.h"

// ─────────────────────────────────────────────────────────────
//  xorshift32 — простой ГПСЧ с периодом 2³²−1.
//
//  Выбран вместо std::mt19937 по двум причинам:
//    1) воспроизводимость не зависит от реализации stdlib;
//    2) минимальные накладные расходы (один регистр состояния).
//
//  Для задач ЦОС качество случайности более чем достаточно:
//  спектр последовательности xorshift32 практически плоский
//  на длинах до 10⁶ отсчётов.
// ─────────────────────────────────────────────────────────────

static uint32_t xorshift32(uint32_t& state)
{
    state ^= state << 13;
    state ^= state >> 17;
    state ^= state << 5;
    return state;
}

// Преобразовать uint32 в float на отрезке [−1, +1]
static float uint32_to_uniform(uint32_t v)
{
    // v ∈ [0, 2³²−1]  →  v / 2³¹ ∈ [0, 2)  →  − 1 ∈ [−1, +1)
    return static_cast<float>(v) / 2147483648.0f - 1.0f;
}

// ═══════════════════════════════════════════════════════════════
//  generate_white_noise
//
//  Генерирует последовательность длины length, каждый отсчёт
//  равномерно распределён на [−1, +1].
//
//  Параметр seed задаёт начальное состояние ГПСЧ.
//  При одинаковом seed результат идентичен на любой платформе.
//
//  Амплитуда сигнала: пиковая ±1, среднеквадратичная ≈ 1/√3.
// ═══════════════════════════════════════════════════════════════

std::vector<sample_t> generate_white_noise(size_t length, uint32_t seed)
{
    // xorshift32 не работает с нулевым состоянием
    if (seed == 0) seed = 1;

    uint32_t state = seed;
    std::vector<sample_t> x(length);

    for (size_t n = 0; n < length; ++n) {
        uint32_t r = xorshift32(state);
        x[n] = uint32_to_uniform(r);
    }

    return x;
}

// ═══════════════════════════════════════════════════════════════
//  generate_multitone
//
//  Генерирует сумму синусоид:
//
//    x[n] = amplitude · Σ sin(2π · f_i · n / fs)
//
//  Число частот определяется размером вектора freqs.
//  Амплитуда каждой компоненты одинакова и равна amplitude;
//  суммарная пиковая амплитуда может достигать
//  amplitude · len(freqs), поэтому для предотвращения
//  клиппирования рекомендуется amplitude ≤ 1/len(freqs).
//
//  Типичное использование: подать частоты из полосы
//  пропускания и полосы заграждения, после фильтрации
//  убедиться, что первые прошли, а вторые подавились.
// ═══════════════════════════════════════════════════════════════

std::vector<sample_t> generate_multitone(size_t length,
                                         real_t fs,
                                         const std::vector<real_t>& freqs,
                                         real_t amplitude)
{
    std::vector<sample_t> x(length, 0.0f);

    for (size_t i = 0; i < freqs.size(); ++i) {
        real_t omega = 2.0 * PI * freqs[i] / fs;

        for (size_t n = 0; n < length; ++n) {
            x[n] += static_cast<sample_t>(
                amplitude * std::sin(omega * static_cast<real_t>(n))
            );
        }
    }

    return x;
}

// ═══════════════════════════════════════════════════════════════
//  generate_impulse
//
//  Единичный импульс δ[n]:
//    x[0] = 1,  x[n] = 0  при n > 0.
//
//  Выход линейного фильтра на δ[n] — его импульсная
//  характеристика h[n].  Это самый прямой способ проверки:
//
//    y_direct[n]  ?=  y_cascade[n]  ?=  h[n]
//
//  Длина последовательности должна быть не менее N
//  (длины фильтра), чтобы вся импульсная характеристика
//  уместилась в выходе.  Рекомендуется length ≥ 2·N для
//  удобства (хвостовые нули подтверждают отсутствие
//  артефактов).
// ═══════════════════════════════════════════════════════════════

std::vector<sample_t> generate_impulse(size_t length)
{
    std::vector<sample_t> x(length, 0.0f);

    if (length > 0) {
        x[0] = 1.0f;
    }

    return x;
}
