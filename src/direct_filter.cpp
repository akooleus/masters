// ═══════════════════════════════════════════════════════════════
//  direct_filter.cpp
//  Прямая (трансверсальная) фильтрация КИХ-фильтром.
//
//  Собственная реализация на основе кольцевого буфера.
//  Используется как опорный вариант для сравнения с каскадной формой.
//
//  Алгоритм:
//    y[m] = Σ_{n=0}^{N-1} h[n] · x[m − n]
//
//  Кольцевой буфер хранит последние N входных отсчётов.
//  При каждом вызове push() новый отсчёт записывается
//  в текущую позицию, вычисляется свёртка, и индекс
//  сдвигается по модулю N.
//
//  Для симметричных фильтров (h[n] = h[N−1−n]) применяется
//  оптимизация: попарное сложение отсчётов перед умножением,
//  что сокращает число умножений вдвое.  Здесь реализована обычная
//  свёртка, чтобы прямой и каскадный пути сравнивались без
//  дополнительной оптимизации только в одном из вариантов.
// ═══════════════════════════════════════════════════════════════

#include "cascade_fir.h"

// ═══════════════════════════════════════════════════════════════
//  DirectFilterState — инициализация
// ═══════════════════════════════════════════════════════════════

void DirectFilterState::init(const std::vector<real_t>& coeffs)
{
    h   = coeffs;
    buf.assign(coeffs.size(), 0.0f);
    idx = 0;
}

// ═══════════════════════════════════════════════════════════════
//  DirectFilterState — сброс состояния (обнуление буфера)
// ═══════════════════════════════════════════════════════════════

void DirectFilterState::reset()
{
    std::fill(buf.begin(), buf.end(), 0.0f);
    idx = 0;
}

// ═══════════════════════════════════════════════════════════════
//  DirectFilterState — подать один отсчёт, вернуть выход
//
//  Кольцевой буфер:
//    buf[idx] ← x           (записать новый отсчёт)
//    y = Σ h[n] · buf[(idx − n) mod N]
//    idx ← (idx + 1) mod N
//
//  Обход буфера организован двумя циклами (без деления
//  по модулю на каждом шаге), что даёт лучшую производи-
//  тельность за счёт предсказуемости ветвлений.
// ═══════════════════════════════════════════════════════════════

sample_t DirectFilterState::push(sample_t x)
{
    const unsigned N = static_cast<unsigned>(h.size());

    // Записать новый отсчёт в кольцевой буфер
    buf[idx] = x;

    // Вычислить свёртку: y = Σ h[n] · x[m − n]
    //
    // x[m]     = buf[idx]
    // x[m − 1] = buf[idx − 1]  (с учётом заворачивания)
    // ...
    // x[m − n] = buf[(idx − n + N) mod N]
    //
    // Разобьём на два участка, чтобы не считать модуль
    // в каждой итерации:
    //   n = 0 .. idx           →  buf[idx − n]
    //   n = idx+1 .. N−1       →  buf[idx − n + N]

    double acc = 0.0;   // аккумулятор в double для точности

    // Участок 1: n = 0 .. idx  (обратный ход от idx до 0)
    {
        unsigned buf_pos = idx;
        unsigned n = 0;
        unsigned count = idx + 1;   // сколько элементов в участке 1
        if (count > N) count = N;

        for (; n < count; ++n) {
            acc += static_cast<double>(h[n]) * static_cast<double>(buf[buf_pos]);
            if (buf_pos == 0) {
                ++n;
                break;
            }
            --buf_pos;
        }

        // Участок 2: оставшиеся n .. N−1 (если есть)
        buf_pos = N - 1;
        for (; n < N; ++n) {
            acc += static_cast<double>(h[n]) * static_cast<double>(buf[buf_pos]);
            --buf_pos;
        }
    }

    // Сдвинуть индекс кольцевого буфера
    idx = (idx + 1) % N;

    return static_cast<sample_t>(acc);
}

// ═══════════════════════════════════════════════════════════════
//  filter_direct — пакетная фильтрация (прямая форма)
//
//  Создаёт состояние фильтра, пропускает весь входной сигнал
//  поотсчётно и возвращает выходную последовательность.
// ═══════════════════════════════════════════════════════════════

std::vector<sample_t> filter_direct(const DirectFIR& fir,
                                    const std::vector<sample_t>& x)
{
    DirectFilterState state;
    state.init(fir.h);

    std::vector<sample_t> y(x.size());

    for (size_t m = 0; m < x.size(); ++m) {
        y[m] = state.push(x[m]);
    }

    return y;
}

std::vector<double> filter_direct_double(const DirectFIR& fir,
                                         const std::vector<double>& x)
{
    DoubleFilterState state;
    state.init(fir.h);

    std::vector<double> y(x.size(), 0.0);
    for (size_t n = 0; n < x.size(); ++n) {
        y[n] = state.push(x[n]);
    }
    return y;
}
